package test.learning.cutom;

public class MobileSpecifications {

	public String mbRAM;
	public String mbProcessor;
	public String mbCameraPixel;
	public String mbBatteryCapacity;
	public String mbInch;
	public String mbWarranty;
	
	public String getMbRAM() {
		return mbRAM;
	}
	public void setMbRAM(String mbRAM) {
		this.mbRAM = mbRAM;
	}
	public String getMbProcessor() {
		return mbProcessor;
	}
	public void setMbProcessor(String mbProcessor) {
		this.mbProcessor = mbProcessor;
	}
	public String getMbCameraPixel() {
		return mbCameraPixel;
	}
	public void setMbCameraPixel(String mbCameraPixel) {
		this.mbCameraPixel = mbCameraPixel;
	}
	public String getMbBatteryCapacity() {
		return mbBatteryCapacity;
	}
	public void setMbBatteryCapacity(String mbBatteryCapacity) {
		this.mbBatteryCapacity = mbBatteryCapacity;
	}
	public String getMbInch() {
		return mbInch;
	}
	public void setMbInch(String mbInch) {
		this.mbInch = mbInch;
	}
	public String getMbWarranty() {
		return mbWarranty;
	}
	public void setMbWarranty(String mbWarranty) {
		this.mbWarranty = mbWarranty;
	}

}
